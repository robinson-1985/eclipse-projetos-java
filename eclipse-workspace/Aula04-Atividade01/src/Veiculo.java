
public class Veiculo {
	
	private String placa;
	private Chassi chassi;
	private Carretinha carretinha;
	
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	
	public String getPlaca() {
		return placa;
	}
	
	public void setChassi(Chassi chassi) {
		this.chassi = chassi;
	}
	
	public Chassi getChassi() {
		return chassi;
	}
	
	
	
	public void setCarretinha(Carretinha carretinha) {
		this.carretinha = carretinha;
	}
	
	public Carretinha getCarretinha() {
		return carretinha;
	}
	
}
