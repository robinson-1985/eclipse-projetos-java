
public class Estacionamento {
	private String nome;
	private Moto moto[];
	private Carro carro[];
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Moto[] getMoto() {
		return moto;
	}
	public void setMoto(Moto[] moto) {
		this.moto = moto;
	}
	public Carro[] getCarro() {
		return carro;
	}
	public void setCarro(Carro[] carro) {
		this.carro = carro;
	}
}
